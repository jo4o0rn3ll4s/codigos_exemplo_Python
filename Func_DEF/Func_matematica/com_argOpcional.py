def soma(val1 = 0, val2 = 0, val3 = 0):
    resul = val1 + val2 + val3
    return resul

def sub(val1 = 0, val2 = 0, val3 = 0):
    resul = val1 - val2 - val3
    return resul

def mult(val1 = 1, val2 = 1, val3 = 1):
    resul = val1 * val2 * val3
    return resul

def div(val1 = 1, val2 = 1, val3 = 1):
    resul = (val1/val2)/val3
    return resul
